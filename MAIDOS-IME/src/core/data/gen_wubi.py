"""Generate Wubi 86 mapping table as JSON."""
import json
import os

t = {}

def add(code, char, freq=500):
    code = code.lower()
    if code not in t:
        t[code] = []
    for entry in t[code]:
        if entry['char'] == char:
            return
    t[code].append({'char': char, 'freq': freq})

# Batch add: list of (code, char, freq)
def batch(entries):
    for e in entries:
        if len(e) == 3:
            add(e[0], e[1], e[2])
        else:
            add(e[0], e[1])

